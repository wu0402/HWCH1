#include <stdio.h>

int main(void)
{
    /* 使用五個 printf 印出 */
    printf("    *\n");
    printf("   ***\n");
    printf("  *****\n");
    printf(" *******\n");
    printf("*********\n\n");

    /* 使用單一 printf 印出相同圖案 */
    printf("    *\n   ***\n  *****\n *******\n*********\n");

    return 0;
}
